function func=hokan(x,delta,L)
func2=zeros(6,size(x,2));
if L==0
    func=func2;
else
    for i=1:length(x)
    	func2(:,i)=...
            [1-x(i)/L,0,0,0,0,0,x(i)/L,0,0,0,0,0;... 
            0,-(3*x(i)^2)/L^2+(2*x(i)^3)/L^3+1,0,0,0,-(2*x(i)^2)/L+x(i)^3/L^2+x(i),0,(3*x(i)^2)/L^2-(2*x(i)^3)/L^3,0,0,0,x(i)^3/L^2-x(i)^2/L;... 
            0,0,-(3*x(i)^2)/L^2+(2*x(i)^3)/L^3+1,0,(2*x(i)^2)/L-x(i)^3/L^2-x(i),0,0,0,(3*x(i)^2)/L^2-(2*x(i)^3)/L^3,0,x(i)^2/L-x(i)^3/L^2,0;... 
            0,0,0,1-x(i)/L,0,0,0,0,0,x(i)/L,0,0;... 
            0,0,(6*x(i))/L^2-(6*x(i)^2)/L^3,0,-(4*x(i))/L+(3*x(i)^2)/L^2+1,0,0,0,(6*x(i)^2)/L^3-(6*x(i))/L^2,0,(3*x(i)^2)/L^2-(2*x(i))/L,0;... 
            0,(6*x(i)^2)/L^3-(6*x(i))/L^2,0,0,0,-(4*x(i))/L+(3*x(i)^2)/L^2+1,0,(6*x(i))/L^2-(6*x(i)^2)/L^3,0,0,0,(3*x(i)^2)/L^2-(2*x(i))/L] ...
            *delta;%max(i)imaで計算したN(x(i))とdeltaをかけています。 
     end
     func=func2;
end
end

